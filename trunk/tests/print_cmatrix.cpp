std::ostream&
operator<<(std::ostream& os, const matrix_d44& m)
{
    os << "[" << endl;
    for(size_t i = 0; i < 4; ++i) {
        os << "  [";
        for(size_t j = 0; j < 4; ++j) {
            os << " " << m[i][j];
        }
        os << " ]" << endl;
    }
    os << "]";
    return os;
}

